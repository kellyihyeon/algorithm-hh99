# [2, 4, 1, 6, 9]
#   -> ->
# [2, 1, 4, 6, 9]
#   ->
# [1, 2, 4, 6, 9]
#>> 화살표: 반복문 == 4번, 3번, 2번, 1번. / 4번 돌고 끝이 났다.(input-1만큼)
# for i in range(5 - 1):
